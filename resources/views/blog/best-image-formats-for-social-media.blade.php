            <h2>JPG vs PNG vs WebP — Which to Use?</h2>
            <ul>
                <li><strong>JPG:</strong> Best for photographs. Small file size, no transparency. Use for most social media posts.</li>
                <li><strong>PNG:</strong> Best for graphics, logos, and images with text. Supports transparency. Larger file size.</li>
                <li><strong>WebP:</strong> Best compression with quality. Supported by most platforms now, but JPG/PNG are safer for maximum compatibility.</li>
            </ul>

